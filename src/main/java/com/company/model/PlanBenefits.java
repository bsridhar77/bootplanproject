package com.company.model;

public class PlanBenefits {

	String planBenefitDetail;
	
	String planDeductibles;
	
	String planDiscount;

	public String getPlanBenefitDetail() {
		return planBenefitDetail;
	}

	public void setPlanBenefitDetail(String planBenefitDetail) {
		this.planBenefitDetail = planBenefitDetail;
	}

	public String getPlanDiscount() {
		return planDiscount;
	}

	public void setPlanDiscount(String planDiscount) {
		this.planDiscount = planDiscount;
	}

	public String getPlanBenefits() {
		return planBenefitDetail;
	}

	public void setPlanBenefits(String planBenefits) {
		this.planBenefitDetail = planBenefits;
	}

	public String getPlanDeductibles() {
		return planDeductibles;
	}

	public void setPlanDeductibles(String planDeductibles) {
		this.planDeductibles = planDeductibles;
	}
}
